    private void Buscar_UltimoTxt() {
        Codigo_db c_db = new Codigo_db();
        ArrayList<String[]> lista = c_db.Buscar_Compras();
        if (!lista.isEmpty()) {
            String compras_produtos = "";
            for (int i = 0; i < lista.size(); i++) {
                if (lista.get(i)[1].equals("true")) {
                    compras_produtos += lista.get(i)[0] + "\t" + "Femenino" + "\t"
                            + lista.get(i)[2] + "\t" + lista.get(i)[3] + System.lineSeparator();
                } else {
                    compras_produtos += lista.get(i)[0] + "\t" + "Masculino" + "\t"
                            + lista.get(i)[2] + "\t" + lista.get(i)[3] + System.lineSeparator();
                }

            }
            jTextArea3.setText(compras_produtos);
        }

    }

    private void Buscar_PenultimoTxt(ArrayList<String[]> lista) {
        if (!lista.isEmpty()) {
            String compras_produtos = "";
            for (int i = 0; i < lista.size(); i++) {
                compras_produtos += lista.get(i)[0] + "\t" + lista.get(i)[1]
                        + "\t" + lista.get(i)[2] + System.lineSeparator();
            }
            jTextArea2.setText(compras_produtos);
        }
    }

    private void Reporte_1(ArrayList<String> lista) {
        if (!lista.isEmpty()) {
            String compras_produtos = "";
            for (int i = 0; i < lista.size(); i++) {
                compras_produtos += lista.get(i) + System.lineSeparator();
            }
            jTextArea1.setText(compras_produtos);
        }
    }
